        public void Update(GameTime gameTime)
        {
            switch (Enum_stage)
            {
                case Stage.stage0:
                    Int_level = stage0;
                    tex_level = li_tex_level[0];

                    li_obj_chest[0].Update(gameTime);

                    Current_State_Info = 0;
                    break;
                case Stage.stage1:
                    Int_level = stage1;
                    tex_level = li_tex_level[1];

                    li_obj_chest[1].Update(gameTime);
                    li_obj_chest[2].Update(gameTime);
                    li_obj_chest[3].Update(gameTime);

                    li_obj_key[0].Update(gameTime);

                    Current_State_Info = 1;
                    break;
                case Stage.stage2:
                    Int_level = stage2;
                    tex_level = li_tex_level[2];

                    li_obj_chest[4].Update(gameTime);
                    li_obj_chest[5].Update(gameTime);

                    li_obj_key[1].Update(gameTime);
                    li_obj_key[2].Update(gameTime);

                    Current_State_Info = 2;
                    break;
                case Stage.stage3:
                    Int_level = stage3;
                    tex_level = li_tex_level[3];

                    li_obj_chest[6].Update(gameTime);
                    li_obj_chest[7].Update(gameTime);
                    li_obj_chest[8].Update(gameTime);

                    li_obj_key[3].Update(gameTime);
                    li_obj_key[4].Update(gameTime);

                    Current_State_Info = 3;
                    break;
                case Stage.stageEnd:
                    Int_level = stageEnd;
                    tex_level = li_tex_level[4];
                    break;
                default:
                    break;
            }

            foreach (Key currentKey in li_obj_key)
            {
                if (!currentKey.boo_loot)
                {
                    currentKey.Update(gameTime);
                }
            }
        }

